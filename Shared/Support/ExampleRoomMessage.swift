import LiveKit

struct ExampleRoomMessage: Identifiable, Equatable, Hashable, Codable {
    let id: String
    let message: String
    let timestamp: Int

    static func == (lhs: Self, rhs: Self) -> Bool {
        lhs.id == rhs.id
    }

    func hash(into hasher: inout Hasher) {
        hasher.combine(id)
    }
}
